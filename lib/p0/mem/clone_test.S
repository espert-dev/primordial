# ===========================================================================
# Memory cloning tests
# ===========================================================================

#include <millicode.S>
#include <testing.S>


# ===========================================================================
# Read-only test data
# ===========================================================================

.section .rodata

.equiv nil, 0
.equiv nil_size, 0


# ===========================================================================
# Test cases
# ===========================================================================

.section .text

test_case Clone_nil_returns_nil
	save_0

	li a0, nil_size
	li a1, nil
	call "mem.Clone"

	expect_z a0
	expect_z a1

	restore_0
end_test
